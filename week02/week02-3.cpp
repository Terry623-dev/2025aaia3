///week02-3.cpp 使用 c++ 2011 年薪版的字串 stoi()功能
///在 CodeBlocks 裡,必須開啟 Settings-Compiler...把 c++ 打勾勾
///SOIT106_ADVANCE_001 Using C++
#include <iostream> /// cin cout 讀入資料和印出資料
#include <string>/// string 字串得功能
using namespace std; ///使用[命名空間]標準的 std
int main()
{
	string a; ///宣告字串
	cin >> a; ///讀入字串

	string ans; ///宣告自串 ans 放答案用的
	int N = a.length(); ///字串 a 的長度
	for(int i=N-1; i>=0; i--){ ///倒過來用的迴圈
		ans += a[i]; ///在迴圈裡, 把a[i] 塞到ans得後面
	}

	cout << a << '+' << stoi(ans) << '='
		<< stoi(a) + stoi(ans) << endl;
} /// stoi() is "string to int" 把[字串]變成[整數]
